     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $payment = Payment::where('userid',Auth::user()->id)->first();
        if(empty($payment))
        {
          return redirect('/payment/index')->with('message','Complete your Payment!');
        }
        else {
          $data = new Finalsubmitted;
          $data->userid = $request->input('userid');
          $data->status = 'Submitted!';
          $data->save();
          return redirect('/home')->with('message','Application Submitted Successfully!');
        }

    }

